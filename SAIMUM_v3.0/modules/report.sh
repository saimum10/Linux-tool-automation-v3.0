generate_report() {
    local target="$1"
    show_stage 9 "REPORTING"

    generate_txt_report "$target"

    case "$REPORT_FORMAT" in
        2|3)  generate_html_report "$target" ;;
    esac
    case "$REPORT_FORMAT" in
        3)    generate_pdf_report  "$target" ;;
    esac
    case "$OUTPUT_FORMAT" in
        2|3|4) generate_json_report "$target" ;;
    esac

    save_to_sqlite
    show_terminal_dashboard
}
