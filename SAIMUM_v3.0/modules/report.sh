generate_json_report() {
    local target="$1"
    local rfile="$OUTPUT_DIR/${REPORT_FILENAME}.json"

    # Write Python script to temp file to avoid heredoc/variable expansion issues
    local pyscript="/tmp/saimum_json_$$.py"
    cat > "$pyscript" << 'PYEOF'
import json, os, re, datetime, sys

output_dir = sys.argv[1]
target     = sys.argv[2]
scan_mode  = int(sys.argv[3])
summary    = json.loads(sys.argv[4])
tinfo      = json.loads(sys.argv[5])
tu         = sys.argv[6].split() if sys.argv[6] else []
ts         = sys.argv[7].split() if sys.argv[7] else []

findings = []
for f in sorted(os.listdir(output_dir)):
    if f.endswith('_bangla.txt'):
        tool = f.replace('_bangla.txt', '')
        try:
            with open(os.path.join(output_dir, f)) as fh:
                content = fh.read()
        except Exception:
            content = ''
        sev = 'info'
        if 'CRITICAL' in content: sev = 'critical'
        elif 'HIGH' in content:   sev = 'high'
        elif 'MEDIUM' in content: sev = 'medium'
        elif 'LOW' in content:    sev = 'low'
        findings.append({'tool': tool, 'severity': sev, 'content': content[:500]})

cves = set()
for f in os.listdir(output_dir):
    if f.endswith('.txt'):
        try:
            with open(os.path.join(output_dir, f)) as fh:
                cves.update(re.findall(r'CVE-\d+-\d+', fh.read()))
        except Exception:
            pass

report = {
    'tool': 'SAIMUM v3.0',
    'target': target,
    'scan_date': datetime.datetime.now().isoformat(),
    'scan_mode': scan_mode,
    'summary': summary,
    'target_info': tinfo,
    'cves': sorted(list(cves)),
    'findings': findings,
    'tools_used': tu,
    'tools_skipped': ts
}
print(json.dumps(report, indent=2))
PYEOF

    # Build JSON args safely
    local summary_json
    summary_json=$(printf '{"critical":%d,"high":%d,"medium":%d,"low":%d,"info":%d}' \
        "$COUNT_CRITICAL" "$COUNT_HIGH" "$COUNT_MEDIUM" "$COUNT_LOW" "$COUNT_INFO")
    local tinfo_json
    tinfo_json=$(printf '{"ip":"%s","domain":"%s","cms":"%s","cdn":"%s","waf":"%s"}' \
        "${TARGET_IP:-}" "${TARGET_DOMAIN:-}" "${DETECTED_CMS:-}" \
        "${CDN_NAME:-}" "${WAF_NAME:-}")

    python3 "$pyscript" \
        "$OUTPUT_DIR" "$target" "$SCAN_MODE" \
        "$summary_json" "$tinfo_json" \
        "${TOOLS_USED[*]:-}" "${TOOLS_SKIPPED[*]:-}" \
        > "$rfile" 2>/dev/null || \
        echo "{\"target\":\"$target\",\"critical\":$COUNT_CRITICAL}" > "$rfile"

    rm -f "$pyscript"
    log_success "JSON Report → $rfile"
    reg_file "report_json" "$rfile"
}

generate_pdf_report() {
    local target="$1"
    command -v wkhtmltopdf &>/dev/null || { log_skip "wkhtmltopdf (PDF)"; return; }
    local html="$OUTPUT_DIR/${REPORT_FILENAME}.html"
    [ ! -f "$html" ] && { log_warn "HTML নেই — PDF skip।"; return; }
    local rfile="$OUTPUT_DIR/${REPORT_FILENAME}.pdf"

    run_tool "wkhtmltopdf" \
        "wkhtmltopdf --page-size A4 --margin-top 10 --margin-bottom 10 \
         --margin-left 10 --margin-right 10 --encoding UTF-8 \
         --no-background --quiet $html $rfile" \
        "/dev/null" 60

    [ -f "$rfile" ] && log_success "PDF Report → $rfile"
    reg_file "report_pdf" "$rfile"
}

