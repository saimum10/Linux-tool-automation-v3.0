generate_txt_report() {
    local target="$1"
    local rfile="$OUTPUT_DIR/${REPORT_FILENAME}.txt"

    {
        echo "================================================================"
        echo "   SAIMUM v3.0 — PENTEST REPORT"
        echo "================================================================"
        echo "Target      : $target"
        echo "Scan Date   : $(date)"
        echo "Scan Mode   : $([ "$SCAN_MODE" -eq 1 ] && echo 'Stealth' || ([ "$SCAN_MODE" -eq 2 ] && echo 'Normal' || echo 'Aggressive'))"
        echo "Duration    : $(elapsed_total)"
        echo "Output Dir  : $OUTPUT_DIR"
        echo ""
        echo "================================================================"
        echo "   EXECUTIVE SUMMARY"
        echo "================================================================"
        echo "🔴 Critical : $COUNT_CRITICAL"
        echo "🟠 High     : $COUNT_HIGH"
        echo "🟡 Medium   : $COUNT_MEDIUM"
        echo "🟢 Low      : $COUNT_LOW"
        echo "🔵 Info     : $COUNT_INFO"
        local total=$(( COUNT_CRITICAL + COUNT_HIGH + COUNT_MEDIUM + COUNT_LOW ))
        echo ""
        echo "Total Findings: $total"
        echo ""

        # Risk Rating
        if [ "$COUNT_CRITICAL" -gt 0 ]; then
            echo "⚠️  OVERALL RISK: 🔴 CRITICAL — অবিলম্বে remediation প্রয়োজন!"
        elif [ "$COUNT_HIGH" -gt 0 ]; then
            echo "⚠️  OVERALL RISK: 🟠 HIGH"
        elif [ "$COUNT_MEDIUM" -gt 0 ]; then
            echo "⚠️  OVERALL RISK: 🟡 MEDIUM"
        else
            echo "✅ OVERALL RISK: 🟢 LOW / INFO"
        fi

        echo ""
        echo "================================================================"
        echo "   TARGET INFORMATION"
        echo "================================================================"
        echo "IP Address  : ${TARGET_IP:-N/A}"
        echo "Domain      : ${TARGET_DOMAIN:-N/A}"
        echo "CMS         : ${DETECTED_CMS:-Not detected}"
        echo "Framework   : ${DETECTED_FRAMEWORK:-Not detected}"
        echo "CDN         : $([ "$HAS_CDN" -eq 1 ] && echo "$CDN_NAME" || echo 'None detected')"
        echo "WAF         : $([ "$HAS_WAF" -eq 1 ] && echo "$WAF_NAME" || echo 'None detected')"
        echo ""
        echo "Open Ports  : ${OPEN_PORTS[*]:-None}"
        echo "Subdomains  : ${#FOUND_SUBDOMAINS[@]} found"
        echo "Emails      : ${#FOUND_EMAILS[@]} found"
        echo "Live Hosts  : ${#LIVE_SUBDOMAINS[@]} found"
        echo ""

        echo "================================================================"
        echo "   DETAILED FINDINGS"
        echo "================================================================"
        echo ""

        # Concat all bangla analysis files
        for f in "$OUTPUT_DIR"/*_bangla.txt; do
            [ -f "$f" ] && { cat "$f"; echo ""; }
        done

        echo ""
        echo "================================================================"
        echo "   CVE REFERENCES"
        echo "================================================================"
        grep -roh "CVE-[0-9]\+-[0-9]\+" "$OUTPUT_DIR"/*.txt 2>/dev/null | \
            sort -u | while read -r cve; do
            echo "  $cve → https://nvd.nist.gov/vuln/detail/$cve"
        done

        echo ""
        echo "================================================================"
        echo "   TOOL TIMELINE"
        echo "================================================================"
        for tool in "${!TOOL_DURATIONS[@]}"; do
            local d="${TOOL_DURATIONS[$tool]}"
            printf "  %-35s %dm %ds\n" "$tool" "$((d/60))" "$((d%60))"
        done

        echo ""
        echo "   Tools Used    : ${#TOOLS_USED[@]}"
        echo "   Tools Skipped : ${#TOOLS_SKIPPED[@]}"
        echo "   Skipped       : ${TOOLS_SKIPPED[*]:-none}"

        echo ""
        echo "================================================================"
        echo "   REMEDIATION PRIORITIES"
        echo "================================================================"
        local prio=1
        grep -rh "CRITICAL:" "$OUTPUT_DIR"/*_bangla.txt 2>/dev/null | \
            grep -v "^=\|^$" | head -10 | while read -r l; do
            echo "  [$prio] 🔴 $l"; ((prio++))
        done
        grep -rh "HIGH:" "$OUTPUT_DIR"/*_bangla.txt 2>/dev/null | \
            grep -v "^=\|^$" | head -10 | while read -r l; do
            echo "  [$prio] 🟠 $l"; ((prio++))
        done

        echo ""
        echo "================================================================"
        echo "   Report generated by SAIMUM v3.0"
        echo "   For educational and authorized testing purposes only."
        echo "================================================================"
    } | tee "$rfile"

    log_success "TXT Report → $rfile"
    reg_file "report_txt" "$rfile"
}

